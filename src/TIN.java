    private void btbetriebsnummerMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btbetriebsnummerMouseClicked
    {//GEN-HEADEREND:event_btbetriebsnummerMouseClicked
        /* Erstellen einer Betriebsnummer, also eine achtstellige
         * Zufallszahl. Die achte Stelle ist eine Pr√ºfsumme, f√ºr diese
         * wird zun√§chst die Quersumme aus den sieben Zufallszahlen gebildet.
         * Restwert aus Mod10 ergibt dann die Pr√ºfsumme.*/
        int[] betrzahlen = new int[8];
        int[] betrzahlenqs = new int[7];
        StringBuilder sb2 = new StringBuilder();
        StringBuilder sb3 = new StringBuilder();
        int maxLength = 10; //L√§nge f√ºr den Long der Quersummenberechnung. Der umgewandelte String sollte nicht l√§nger als 10 werden.
        String ausgabebetriebsnummer;
        String betrnrquersumme;
        long betrnrquersummeLong;
        int i;
        //Bilden der Zufallszahlen von 0-9
        for (i = 0; i < betrzahlen.length - 1; i++) {
            betrzahlen[i] = (int) (Math.random() * 9);
        }
        if ((betrzahlen[0] == 1 && betrzahlen[1] == 0) || (betrzahlen[0] == 0 && betrzahlen[1] == 0)) {
            betrzahlen[1] = 1;
        }
        if (betrzahlen[1] == 1 && betrzahlen[2] == 0) {
            betrzahlen[2] = 1;
        }
        //Verrechnen der Zahlen mit der Vorgegebenen Wichtung in separates Array
        betrzahlenqs[0] = betrzahlen[0];
        betrzahlenqs[1] = betrzahlen[1] * 2;
        betrzahlenqs[2] = betrzahlen[2];
        betrzahlenqs[3] = betrzahlen[3] * 2;
        betrzahlenqs[4] = betrzahlen[4];
        betrzahlenqs[5] = betrzahlen[5] * 2;
        betrzahlenqs[6] = betrzahlen[6];

        //Bilden einer Stringzahl
        for (int x : betrzahlenqs) {
            sb3.append(x);
        }
        betrnrquersumme = sb3.toString();

        betrnrquersummeLong = Long.parseLong(betrnrquersumme, maxLength); //Der String

        //Setzen der Pr√ºfsumme f√ºr die achte Stelle durch die Funktion "quersumme"
        betrzahlen[7] = pruefsumme(betrnrquersummeLong);
        //Bilden eines Strings aus den Werten des Arrays f√ºr die Ausgabe
        for (int y : betrzahlen) {
            sb2.append(y);
        }

        ausgabebetriebsnummer = sb2.toString();
        edbetriebsnummer.setText(ausgabebetriebsnummer);

    }//GEN-LAST:event_btbetriebsnummerMouseClicked

